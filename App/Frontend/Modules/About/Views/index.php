Jean Forteroche est né à Châtenay-Malabry, en 1976. Il passe son enfance en Normandie, où ses parents lui transmettent très tôt le goût de la lecture, 
des voyages et de la science-fiction. Encouragé par ses professeurs de français, Forteroche se met à écrire ses premières histoires à l’âge de quinze ans 
et décide de présenter un baccalauréat littéraire, qu’il décroche avec une mention très bien. Après une hypokhâgne et une khâgne au lycée Jeanne d’Arc de 
Rouen, il rejoint Paris et poursuit ses études à la Sorbonne et à Sciences Politiques. Il entame une carrière de journaliste et publie ses premiers récits 
de voyages et d’aventures, parmi lesquels Evasion ou encore Mirage, deux succès d’éditions qui gagneront le prix de la Maison de la Presse et le Prix Sang 
d’encre des lycéens en 2012 et 2014. Billet simple pour l’Alaska est son cinquième roman, qu’il se propose pour la première fois de mettre en ligne à 
travers un blog numérique. Préparez-vous dès à présent à être entrainé dans une contrée sauvage et inhospitalière, en suivant la jeune Charlie, qui 
se retrouve malgré elle confrontée à une affaire de meurtre dès son arrivée à Juneau.
